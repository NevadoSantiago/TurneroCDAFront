const { width, height } = Dimensions.get("window");
const CARD_HEIGHT = 180;
const CARD_WIDTH = width * 0.9;
const SPACING_FOR_CARD_INSET = width * 0.1 - 10;

const MostrarReserva = (props) => {
  //const theme = useTheme();

  console.log('----------- TURNO -----------')
  var turno = props.turno
  console.log(turno)

  const initialMapState = {
    //markers,
    markers: [
      {
        coordinate: {
          latitude: parseFloat(turno.latitud),
          longitude: parseFloat(turno.longitud),
        },
        title: turno.nombreSucursal,
        description: turno.direccion
      }
    ],
    region: {
      latitude: parseFloat(turno.latitud),
      longitude: parseFloat(turno.longitud),
      latitudeDelta: 0.0012,
      longitudeDelta: 0.01,
    },
  };

  const [state, setState] = React.useState(initialMapState);

  let mapIndex = 0;
  let mapAnimation = new Animated.Value(0);

  useEffect(() => {
    mapAnimation.addListener(({ value }) => {
      let index = Math.floor(value / CARD_WIDTH + 0.3); // animate 30% away from landing on the next item
      if (index >= state.markers.length) {
        index = state.markers.length - 1;
      }
      if (index <= 0) {
        index = 0;
      }

      clearTimeout(regionTimeout);

      const regionTimeout = setTimeout(() => {
        if (mapIndex !== index) {
          mapIndex = index;
          const { coordinate } = state.markers[index];
          _map.current.animateToRegion(
            {
              ...coordinate,
              latitudeDelta: state.region.latitudeDelta,
              longitudeDelta: state.region.longitudeDelta,
            },
            350
          );
        }
      }, 10);
    });
  });

  const interpolations = state.markers.map((marker, index) => {
    const inputRange = [
      (index - 1) * CARD_WIDTH,
      index * CARD_WIDTH,
      ((index + 1) * CARD_WIDTH),
    ];

    const scale = mapAnimation.interpolate({
      inputRange,
      outputRange: [1, 1.5, 1],
      extrapolate: "clamp"
    });

    return { scale };
  });

  const onMarkerPress = (mapEventData) => {
    const markerID = mapEventData._targetInst.return.key;

    let x = (markerID * CARD_WIDTH) + (markerID * 20);
    if (Platform.OS === 'ios') {
      x = x - SPACING_FOR_CARD_INSET;
    }

    _scrollView.current.scrollTo({ x: x, y: 0, animated: true })
    //_scrollView.current.scrollTo({ x: x, y: 0, animated: true });
  }

  const _map = React.useRef(null);
  const _scrollView = React.useRef(null);

  return (
    <View style={styles.container}>
      <MapView
        ref={_map}
        initialRegion={state.region}
        style={styles.container}
        provider={PROVIDER_GOOGLE}
        customMapStyle={MAP_STYLE}
      >
        {state.markers.map((marker, index) => {
          const scaleStyle = {
            transform: [
              {
                scale: interpolations[index].scale,
              },
            ],
          };
          return (
            <MapView.Marker key={index} coordinate={marker.coordinate} /*onPress={(e) => onMarkerPress(e)}*/ title={turno.nombreSucursal} description={turno.direccion}>
              <Animated.View style={[styles.markerWrap]}>
                <Animated.Image
                  source={require('../../../../assets/map_marker.png')}
                  style={[styles.marker, scaleStyle]}
                  resizeMode="cover"
                />
              </Animated.View>
            </MapView.Marker>
          );
        })}
      </MapView>

      <Animated.ScrollView
        ref={_scrollView}
        horizontal
        pagingEnabled
        scrollEventThrottle={1}
        showsHorizontalScrollIndicator={false}
        snapToInterval={CARD_WIDTH + 20}
        snapToAlignment="center"
        style={styles.scrollView}
        contentInset={{
          top: 0,
          left: SPACING_FOR_CARD_INSET,
          bottom: 0,
          right: SPACING_FOR_CARD_INSET
        }}
        contentContainerStyle={{
          paddingHorizontal: Platform.OS === 'android' ? SPACING_FOR_CARD_INSET : 0
        }}
        onScroll={Animated.event(
          [
            {
              nativeEvent: {
                contentOffset: {
                  x: mapAnimation,
                }
              },
            },
          ],
          { useNativeDriver: true }
        )}
      >
        {state.markers.map((marker, index) => (
          <View style={styles.card} key={index}>
            <View style={styles.textContent}>
              <Text numberOfLines={1} style={styles.cardtitle}>{marker.title}</Text>
              <Text numberOfLines={1} style={styles.cardDescription}>{marker.description}</Text>
              <Text numberOfLines={1} style={styles.cardtitle}>-</Text>
              <Text numberOfLines={1} style={styles.cardDescription}>{turno.sintomas}</Text>
              <View style={{ flex: 1, flexDirection: 'column', justifyContent: 'flex-end' }}>
                <Button
                  buttonStyle={{ backgroundColor: style.secondary.color, borderRadius: 15, height: 50 }}
                  titleStyle={{
                    justifyContent: 'center',
                    alignItems: 'center',
                    fontFamily: 'Nunito_bold',
                    width: '100%'
                  }}
                  title="Cancelar turno"
                  onPress={() => {
                    Alert.alert('Cancelar', 'Â¿Seguro que desea cancelar la reserva?',
                      [
                        { text: 'SI', onPress: () => { console.warn('SI pressed') } },
                        { text: 'NO', onPress: () => { console.warn('NO pressed') } }
                      ]
                    )
                  }}
                ></Button>
              </View>
            </View>
          </View>
        ))}
      </Animated.ScrollView>
    </View >
  );
};

